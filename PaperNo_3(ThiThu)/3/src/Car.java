
public class Car {
	private String maker;
	private int rate;
	public Car() {
		super();
	}
	public Car(String maker, int rate) {
		super();
		this.maker = maker;
		this.rate = rate;
	}
	public String getMaker() {
		return maker;
	}
	public void setMaker(String maker) {
		this.maker = maker;
	}
	public int getRate() {
		return rate;
	}
	public void setRate(int rate) {
		this.rate = rate;
	}
	
	

}
